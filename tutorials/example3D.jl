# The boundary conditions are defined by creating [`Boundary`](@ref FaADE.Helpers.Boundary) objects, which will then be fed to the PDE structure
BoundaryLeft = SAT_Dirichlet((y,t)->0.0,grid.Î”x, Left, order)
